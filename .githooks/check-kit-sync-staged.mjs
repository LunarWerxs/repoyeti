#!/usr/bin/env node
/**
 * Pre-commit guard: refuse to commit a kit-synced file that was edited AWAY from the kit.
 *
 * Files under the LunarWerx kit's control (lunarwerx-ui/sync.mjs: server libs, their tests, the
 * tray-host launch chain, the shared web families) exist as byte-identical copies in every app.
 * Editing the APP copy forks it: every other app now disagrees, and the sync gate starts failing
 * for everyone until someone works out which copy is canonical.
 *
 * This is not hypothetical. On 2026-07-18 a lint sweep in one app applied biome's `useTemplate` to
 * its own copy of dpapi-seal.test.ts. Nothing stopped the commit (that repo had no hooks at all),
 * and the drift then blocked commits in the KIT repo, which is the one place the fix had to land.
 * The DO-NOT-EDIT header on those files does not stop an editor action or an autofix, so the rule
 * needs teeth at commit time.
 *
 * WHAT IT ALLOWS, deliberately:
 *   - Committing a synced file whose content MATCHES the kit. That is a re-sync, the correct way
 *     for a kit change to reach an app, and blocking it would make `node sync.mjs` uncommittable.
 *   - Drift in files you did NOT stage. Only staged paths are considered, so unrelated drift
 *     elsewhere in the repo cannot hold your commit hostage. A gate that blocks work it has no
 *     business blocking is a gate people learn to reflexively --no-verify, which is worse than
 *     having no gate.
 *
 * It never reimplements the kit's path rules: it runs `sync.mjs --check` and reads that report, so
 * it cannot drift from the real mapping. If the kit checkout is absent (CI, a fresh clone without
 * the sibling repo) or node is unavailable, it exits 0 and stays out of the way.
 *
 * Enable once per repo with:  git config core.hooksPath .githooks
 * Bypass a single commit with:  git commit --no-verify
 *
 * Usage: node .githooks/check-kit-sync-staged.mjs <kit.config app key>
 */
import { execFileSync } from "node:child_process";
import { existsSync } from "node:fs";
import { join, resolve } from "node:path";

const APP = process.argv[2];
if (!APP) {
  console.error("check-kit-sync-staged: missing <app key> argument");
  process.exit(2);
}

const REPO = process.cwd();

/** Locate the sibling kit checkout by walking up; layouts differ (devwebui nests one deeper). */
function findKit(start) {
  let dir = resolve(start);
  for (let i = 0; i < 5; i++) {
    const candidate = join(dir, "lunarwerx-ui", "sync.mjs");
    if (existsSync(candidate)) return join(dir, "lunarwerx-ui");
    const parent = resolve(dir, "..");
    if (parent === dir) break;
    dir = parent;
  }
  return null;
}

const KIT = findKit(REPO);
if (!KIT) process.exit(0); // no kit checkout here — nothing to compare against

const norm = (p) => p.replace(/\\/g, "/").toLowerCase();

// Only files being ADDED/COPIED/MODIFIED matter; a deletion cannot "differ" from the kit.
let staged = [];
try {
  staged = execFileSync("git", ["diff", "--cached", "--name-only", "--diff-filter=ACM"], {
    encoding: "utf8",
  })
    .split("\n")
    .map((s) => s.trim())
    .filter(Boolean)
    .map((p) => norm(resolve(REPO, p)));
} catch {
  process.exit(0); // not a git context we understand — don't invent a failure
}
if (staged.length === 0) process.exit(0);

// Ask the kit itself what is drifted. LUNARWERX_KIT_ALLOW_DIRTY=1 mirrors the kit's own hook: the
// "kit has uncommitted files" half of --check is guaranteed to fire mid-commit and is not our job.
let report = "";
try {
  report = execFileSync("node", [join(KIT, "sync.mjs"), "--check", "--app", APP], {
    cwd: KIT,
    encoding: "utf8",
    env: { ...process.env, LUNARWERX_KIT_ALLOW_DIRTY: "1" },
  });
} catch (err) {
  // --check exits non-zero precisely when there IS drift, which is the case we care about.
  report = `${err.stdout ?? ""}${err.stderr ?? ""}`;
}

// Report lines look like:  "      ! differs  D:/path/to/file.ts"
const offenders = [...report.matchAll(/^\s*!\s+(\S+)\s+(.+?)\s*$/gm)]
  .filter((m) => m[1] === "differs")
  .map((m) => m[2].replace(/\\/g, "/"))
  .filter((p) => staged.includes(norm(p)));

if (offenders.length === 0) process.exit(0);

const lines = [
  "",
  "✗ kit-synced file edited in the app instead of the kit:",
  ...offenders.map((p) => `    ${p}`),
  "",
  "  These files are generated by lunarwerx-ui/sync.mjs and must be byte-identical across every",
  "  app. Editing the app copy forks it and breaks the sync gate for all of them.",
  "",
  "  Fix it at the source:",
  `    1. make the same edit in ${KIT.replace(/\\/g, "/")}/src/...`,
  "    2. node sync.mjs            (re-syncs every app)",
  "    3. commit the kit AND the re-synced app copies",
  "",
  "  Committing one of these files is fine when it MATCHES the kit — that is a re-sync.",
  "  Bypass once with: git commit --no-verify",
  "",
];
console.error(lines.join("\n"));
process.exit(1);
